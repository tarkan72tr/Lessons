public class nIncrementDecrement2 {

    public static void main(String[] args) {


        // pre-increment -> increment the variable first and use it
        // post-increment -> use it and THEN increment it

        int number = 17;

        System.out.println(++number); // 18  //

        int number2 = 17;

        System.out.println(number2++); //17

        System.out.println(number2); // 18




    }
}
