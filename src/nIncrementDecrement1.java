public class nIncrementDecrement1 {

    public static void main(String[] args) {


        int noOfStudents  = 20;

        noOfStudents++; // can be used a statement   -> noOfStudents = noOfStudents + 1 ;

        System.out.println(noOfStudents);  //21

        noOfStudents--;

        System.out.println(noOfStudents); //20

        ++noOfStudents;

        System.out.println(noOfStudents); // 21

        --noOfStudents;

        System.out.println(noOfStudents); //20

    }
}
